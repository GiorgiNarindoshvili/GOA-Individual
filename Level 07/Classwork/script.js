//subject1
const subject1 = prompt("Enter your grade for Geometry: ");

//if else statement to get a final result about sub1
if(subject1 >= 90 && subject1 <= 100){
    console.log("Your Grade is A for Geometry")
}else if(subject1 >= 70 && subject1 <= 80){
    console.log("Your Grade is B for Geometry")
}else if(subject1 >= 50 && subject1 <= 65){
    console.log("Your Grade is C for Geometry")
}else if(subject1 >= 25 && subject1 <= 50){
    console.log("Your Grade is D for Geometry")
}else{
    console.log("Go and Study for Geometry")
}

